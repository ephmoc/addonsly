/// Defines the default [double] values to be used as padding sizes
abstract class Paddings {
  /// padding of [2.0]
  static const double xxSmall = 2;

  /// padding of [4.0]
  static const double xSmall = 4;

  /// padding of [6.0]
  static const double small = 6;

  /// padding of [8.0]
  static const double normal = 8;

  /// padding of [10.0]
  static const double semiNormal = 10;

  /// padding of [12.0]
  static const double semiMedium = 12;

  /// padding of [16.0]
  static const double medium = 16;

  /// padding of [20.0]
  static const double mediumLarge = 20;

  /// padding of [24.0]
  static const double mediumXLarge = 24;

  /// padding of [32.0]
  static const double large = 32;

  /// padding of [36.0]
  static const double xLarge = 36;

  /// padding of [48.0]
  static const double xxLarge = 48;

  /// padding of [64.0]
  static const double xxxLarge = 64;
}
